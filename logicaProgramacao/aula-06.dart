void main() {
  // Calculo de idade
  int anoAtual = 2024;
  int anoNascimento = 2002;
  int idade = anoAtual - anoNascimento;

  print(idade);

  // Adicao (+)
  // Subtacao (-)
  // Multiplicacao (*)
  // Divisao (/)
  // Resto da divisao (%)
  // Precedencia ()

  // Numericos (int, double)

  int numero1 = 10;
  int numero2 = 3;

  int adicao = numero1 + numero2;
  int sub = numero1 - numero2;
  int multi = numero1 * numero2;
  double divisao = numero1 / numero2;
  int resto = numero1 % numero2;
  double procedencia = numero1 / (numero2 + numero1);

  print(adicao);
  print(sub);
  print(multi);
  print(divisao);
  print(resto);
  print(procedencia);
}
